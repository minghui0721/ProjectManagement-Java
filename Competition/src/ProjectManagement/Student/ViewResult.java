    private DefaultCategoryDataset createDataset(List<String> grades) {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        // Count frequency of each mapped grade
        Map<String, Integer> gradeCounts = new HashMap<>();
        for (String grade : grades) {
            String mappedGrade = mapGradeToRange(grade); // Map grade to range
            gradeCounts.put(mappedGrade, gradeCounts.getOrDefault(mappedGrade, 0) + 1);
        }
        // Add mapped grades to dataset
        for (Map.Entry<String, Integer> entry : gradeCounts.entrySet()) {
            dataset.addValue(entry.getValue(), entry.getKey(), entry.getKey()); // Add both key and value as the same value
        }
        return dataset;
    }

    // Method to map the grades to the specified range
    private String mapGradeToRange(String grade) {
        int score = Integer.parseInt(grade);
        if (score >= 80) {
            return "A";
        } else if (score >= 60) {
            return "B";
        } else if (score >= 40) {
            return "C";
        } else if (score >= 30) {
            return "D";
        } else if (score >= 20) {
            return "E";
        } else {
            return "F";
        }
    }

    private void displayHistogram(String studentID) {
        List<String> grades = findGradesByStudentId(studentID);
        if (grades == null || grades.isEmpty()) {
            // No grades found for the given student
            return;
        }

        DefaultCategoryDataset dataset = createDataset(grades);

        JFreeChart chart = ChartFactory.createBarChart(
                "Result Summary Chart", // Title
                "Grade", // X-axis Label
                "Number", // Y-axis Label
                dataset,
                PlotOrientation.VERTICAL,
                true, // Include legend
                true,
                false
        );

        // Customize colors
        CategoryPlot plot = chart.getCategoryPlot();
        BarRenderer renderer = (BarRenderer) plot.getRenderer();
        Map<String, Color> gradeColors = new HashMap<>();
        gradeColors.put("A", Color.decode("#173F5F"));
        gradeColors.put("B", Color.decode("#20639B"));
        gradeColors.put("C", Color.decode("#3CAEA3"));
        gradeColors.put("D", Color.decode("#F6D55C"));
        gradeColors.put("E", Color.decode("#CD5C5C"));
        gradeColors.put("F", Color.decode("#ED553B"));

        // Iterate through each series in the dataset
        for (int i = 0; i < dataset.getRowCount(); i++) {
            Comparable seriesKey = dataset.getColumnKey(i); // Use getColumnKey instead of getRowKey
            String grade = seriesKey.toString();
            Color color = gradeColors.get(grade);
            renderer.setSeriesPaint(i, color);
        }

        // Adjust the width of the bars
        renderer.setMaximumBarWidth(2.0); // Set the width of the bars (0.8 is an example, adjust as needed)

        // Adjust the margin of the category axis to align bars with tick marks
        CategoryAxis xAxis = (CategoryAxis) plot.getDomainAxis();
        xAxis.setLowerMargin(0.2); // Set lower margin to provide space before the first tick mark
        xAxis.setUpperMargin(0.2); // Set upper margin to provide space after the last tick mark

        // Set custom tick labels and positions to align with grades
        xAxis.setTickLabelFont(new Font("SansSerif", Font.PLAIN, 15)); // Set font for tick labels

        // Optional: Adjust plot orientation if needed
        plot.setOrientation(PlotOrientation.VERTICAL); // Ensure that the plot orientation is vertical

        // Iterate through each series in the dataset
        int totalGrades = 0;
        for (int i = 0; i < dataset.getRowCount(); i++) {
            Comparable seriesKey = dataset.getColumnKey(i);
            String grade = seriesKey.toString();
            Color color = gradeColors.get(grade);
            renderer.setSeriesPaint(i, color);
            totalGrades = totalGrades + 1;
        }

        // Set custom range for y-axis based on total grades, multiplied by 2
        NumberAxis yAxis = (NumberAxis) plot.getRangeAxis();
        int maxYAxisValue = totalGrades * 2; // Calculate the maximum value for the y-axis
        yAxis.setRange(0, maxYAxisValue); // Set range based on total grades, multiplied by 2
        yAxis.setTickUnit(new NumberTickUnit(1)); // Set tick unit to 1

        // Set background color of the plot
        plot.setBackgroundPaint(new Color(105, 87, 233)); // Set to any color you prefer

        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new Dimension(800, 300)); // Set preferred size
        jPanel5.setLayout(new BorderLayout());
        jPanel5.add(chartPanel, BorderLayout.CENTER);
        jPanel5.validate();
    }

