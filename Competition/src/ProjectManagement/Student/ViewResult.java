    private String[][] displayGrade() {
        // Read project name and its ID from the text file
        List<String> projects = CourseReader.readCoursesFromFile("project_details.txt");

        // Initialize a two-dimensional array to store project name and ID pairs
        String[][] projectArray = new String[projects.size()][2];

        // Output the retrieved courses for verification
        int index = 0;
        for (String project : projects) {
            String[] parts = project.split("\\|");
            if (parts.length >= 10 && parts[7].equals(student.getIntakeCode())) {
                String projectName = parts[1];
                String projectID = parts[0];

                // Store project name and ID in the array
                projectArray[index][0] = projectName;
                projectArray[index][1] = projectID;
                index++;
            }
        }
        // Return the projectArray
        return projectArray;
    }

    private void checkGrades(String studentID, String[][] projectArray) {
        // Read grading details from the text file
        List<String> gradingDetails = CourseReader.readCoursesFromFile("grading.txt");

        // Iterate over grading details
        for (String detail : gradingDetails) {
            String[] parts = detail.split("\\|");
            if (parts.length >= 7 && parts[1].equals(student.getId())) { // Check if student ID matches
                String projectID = parts[3];
                String grade = parts[4];

                // Search for project ID in the projectArray
                for (String[] project : projectArray) {
                    if (project != null && project.length == 2 && project[1].equals(projectID)) { // Check if project ID matches
                        String projectName = project[0];
                        // Create the project panel
                        JPanel projectPanel = createProjectPanel(projectName, grade);

                        // Add the project panel to jPanel7
                        jPanel6.add(projectPanel);

                        break;
                    }
                }
            }
        }
    }

